// Connect to the Ethereum network using ethers.js
const provider = new ethers.providers.AlchemyProvider(
  "goerli",
  "Tt0bj6lRTcUAxeBpzVucFZE8CSJbulvX"
);
// Create a wallet instance using the private key
const wallet = new ethers.Wallet(privateKey, provider);

const zetaContract = new ethers.Contract(
  zetaContractAddress,
  zetaContractABI,
  wallet
);

// Create a contract instance
const contract = new ethers.Contract(contractAddress, contractABI, wallet);

// Handler function for sending the transaction
export default async function handler(req, res) {
  if (req.method !== "POST") {
    return res.status(405).end(); // Method Not Allowed
  }

  try {
    const { address, amount } = req.body;

    // Convert the amount to Wei
    const amountInWei = ethers.utils.parseEther(amount.toString()).toString();

    // Approve the ERC20 token contract to spend tokens on behalf of the user
    const approvalTx = await zetaContract.approve(contractAddress, amountInWei);

    // Wait for the approval transaction to be mined
    await approvalTx.wait();

    // Call the 'send' function of the contract
    const transaction = await contract.send(
      80001, // Replace with the desired destination chain ID
      address,
      amountInWei
    );

    // Wait for the transaction to be mined
    const receipt = await transaction.wait();

    // Transaction hash and receipt data can be sent as the API response
    res.json({ transactionHash: receipt.transactionHash, receipt: receipt });
  } catch (error) {
    console.error("Error sending transaction:", error);
    res.status(500).json({ error: "Error sending transaction" });
  }
}
